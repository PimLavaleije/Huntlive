export type TranslationKey = keyof typeof nl
export const translations: Record<Lang, Record<TranslationKey, string>> = { nl, en }

export function tr(lang: Lang, key: TranslationKey, replacements?: Record<string, string | number>): string {
  let str = translations[lang][key] ?? translations.nl[key] ?? key
  if (replacements) {
    for (const [k, v] of Object.entries(replacements)) {
      str = str.replace(`{${k}}`, String(v))
    }
  }
  return str
}
